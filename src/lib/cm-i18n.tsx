const DICTS: Record<CMLang, Dict> = { en: EN, km: KM, zh: ZH };

interface CMLangContextValue {
  lang: CMLang;
  setLang: (l: CMLang) => void;
  t: (key: string, vars?: Record<string, string>) => string;
}

const CMLangContext = createContext<CMLangContextValue>({
  lang: "en",
  setLang: () => {},
  t: (key) => key,
});

const STORAGE_KEY = "cm_lang";

function getStoredLang(): CMLang {
  try {
    const v = localStorage.getItem(STORAGE_KEY);
    if (v === "en" || v === "km" || v === "zh") return v;
  } catch { /* */ }
  return "en";
}

export function CMLangProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<CMLang>(getStoredLang);

  useEffect(() => {
    document.documentElement.setAttribute("lang", lang === "en" ? "en" : lang === "km" ? "km" : "zh-CN");
  }, [lang]);

  const setLang = useCallback((l: CMLang) => {
    setLangState(l);
    try { localStorage.setItem(STORAGE_KEY, l); } catch { /* */ }
  }, []);

  const t = useCallback((key: string, vars?: Record<string, string>) => {
    let str = DICTS[lang][key] ?? EN[key] ?? key;
    if (vars) {
      for (const [k, v] of Object.entries(vars)) str = str.replace(`{${k}}`, v);
    }
    return str;
  }, [lang]);

  return <CMLangContext.Provider value={{ lang, setLang, t }}>{children}</CMLangContext.Provider>;
}

export const useCMLang = () => useContext(CMLangContext);
